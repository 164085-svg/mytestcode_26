กว้าง = float(input("ใส่ความกว้าง: "))
ยาว = float(input("ใส่ความยาว: "))

พื้นที่ = กว้าง * ยาว
เส้นรอบรูป = 2 * (กว้าง + ยาว)

# ตรวจสอบประเภทของสี่เหลี่ยม
if กว้าง == ยาว:
    ประเภท = "สี่เหลี่ยมจัตุรัส"
else:
    ประเภท = "สี่เหลี่ยมผืนผ้า"

print("=== ผลการวิเคราะห์ ===")
print(f"รูปทรงนี้คือ: {ประเภท}")
print(f"พื้นที่: {พื้นที่} ตารางหน่วย")
print(f"ความยาวเส้นรอบรูป: {เส้นรอบรูป} หน่วย")